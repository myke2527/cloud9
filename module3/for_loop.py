for letter in 'hello!':
    print('Current letter', letter)  # Iterate through each letter in the string and print the current letter

for counter in range(1, 11):
    print(counter)  # Print numbers from 1 to 10 using a for loop
print('Finished!')

for counter in range(11):
    print(counter)  # Print numbers from 0 to 10 using a for loop
print('Finished!')

for counter in range(1, 11, 2):
    print(counter)  # Print odd numbers from 1 to 10 using a for loop
print('Finished!')
