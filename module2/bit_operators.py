first_bit = 1  # Set the value of the first bit
second_bit = 0  # Set the value of the second bit

print(first_bit & second_bit)  # Bitwise AND operation between first_bit and second_bit
print(first_bit | second_bit)  # Bitwise OR operation between first_bit and second_bit
print(first_bit ^ second_bit)  # Bitwise XOR operation between first_bit and second_bit